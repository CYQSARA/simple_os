SETMODE:
   # Send MISC regs
   MOV $MISC_ADDR, %dx
   MOV (%esi), %AL
   OUT %al, %DX
   call iodelay
   INC %esi

   MOV $STATUS_ADDR, %dx
   MOV (%esi), %AL
   OUT %AL, %dx
   call iodelay
   INC %esi

   # Send SEQ regs
   MOV $0, %CX
REG_LOOP:
   MOV $SEQ_ADDR, %dx
   MOV %CL, %al
   OUT %AL, %dx
   call iodelay

   MOV $SEQ_ADDR, %dx
   INC %DX
   MOV (%esi), %AL
   OUT %AL, %dx
   call iodelay
   INC %esi
   INC %CX
   CMP $5, %cl
   JL REG_LOOP

   # Clear Protection bits
   MOV $0x0E, %ah
   MOV 0x11, %al
   AND 0x7F, %ah
   MOV $CRTC_ADDR, %dx
   OUT %AX, %dx
   call iodelay

   # Send CRTC regs
   MOV $0, %cx
REG_LOOP2:
   MOV $CRTC_ADDR, %dx
   MOV %CL, %al
   OUT %AL, %dx
   call iodelay

   MOV $CRTC_ADDR, %dx
   INC %DX
   MOV (%esi), %AL
   OUT %AL, %dx
   call iodelay

   INC %esi
   INC %CX
   CMP $25, %CL
   JL REG_LOOP2

   # Send GRAPHICS regs
   MOV $0, %CX
REG_LOOP3:
   MOV $GRACON_ADDR, %dx
   MOV %CL, %al
   OUT %AL, %dx
   call iodelay

   MOV $GRACON_ADDR, %dx
   INC %DX
   MOV (%esi), %AL
   OUT %AL, %dx
   call iodelay

   INC %esi
   INC %CX
   CMP $9, %CL
   JL REG_LOOP3

   MOV $STATUS_ADDR, %dx
   IN %DX, %al
   call iodelay

   # Send ATTRCON regs
   MOV $0, %cx
REG_LOOP4:
   MOV $ATTRCON_ADDR, %dx
   IN %DX, %ax

   MOV %CL, %al
   OUT %AL, %dx
   call iodelay

   MOV (%esi), %AL
   OUT %AL, %dx
   call iodelay

   INC %esi
   INC %CX
   CMP $21, %CL
   JL REG_LOOP4

   MOV $0x20, %al
   OUT %AL, %dx
   call iodelay
   RET
