SETPALETTE:
   # Address of the 6-byte array
   MOV $PALETTE, %edi

   MOV $0, %ECX
PAL_LOOP:
   MOV %CX, %ax
   # Send color
   MOV $0x03C8, %dx
   OUT %AL, %dx

   # Write R value
   MOV $0x03C9, %dx
   movb (%edi), %al
   OUT %AL, %dx

   # Write G value
   MOV $0x03C9, %dx
   movb 1(%edi), %al
   OUT %AL, %dx

   # Write B value
   MOV $0x03C9, %dx
   movb 2(%edi), %al
   OUT %AL, %dx

   ADD $3, %edi
   INC %CX
   mov %cl, %al
   CMP $16, %CX
   #CMP $256, %CX
   JLE PAL_LOOP

   RET


