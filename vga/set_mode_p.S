SETMODE:
   # Send MISC regs
   MOV $MISC_ADDR, %dx
   MOV (%si), %AL
   OUT %al, %DX
   call iodelay
   INC %SI

   MOV $STATUS_ADDR, %dx
   MOV (%si), %AL
   OUT %AL, %dx
   call iodelay
   INC %SI

   # Send SEQ regs
   MOV $0, %CX
REG_LOOP:
   MOV $SEQ_ADDR, %dx
   MOV %CL, %al
   OUT %AL, %dx
   call iodelay

   MOV $SEQ_ADDR, %dx
   INC %DX
   MOV (%si), %AL
   OUT %AL, %dx
   call iodelay
   INC %SI
   INC %CX
   CMP $5, %cl
   JL REG_LOOP

   # Clear Protection bits
   MOV $0x0E, %ah
   MOV 0x11, %al
   AND 0x7F, %ah
   MOV $CRTC_ADDR, %dx
   OUT %AX, %dx
   call iodelay

   # Send CRTC regs
   MOV $0, %cx
REG_LOOP2:
   MOV $CRTC_ADDR, %dx
   MOV %CL, %al
   OUT %AL, %dx
   call iodelay

   MOV $CRTC_ADDR, %dx
   INC %DX
   MOV (%si), %AL
   OUT %AL, %dx
   call iodelay

   INC %SI
   INC %CX
   CMP $25, %CL
   JL REG_LOOP2

   # Send GRAPHICS regs
   MOV $0, %CX
REG_LOOP3:
   MOV $GRACON_ADDR, %dx
